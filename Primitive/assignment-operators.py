# addition
x = 10
x += 5  # this is the same as x = x + 5
print(x)  # 15

# subtraction
x = 10
x -= 5  # this is the same as x = x - 5
print(x)  # 5

# multiplication
x = 10
x *= 5  # this is the same as x = x * 5
print(x)  # 50

# division
x = 10
x /= 5  # this is the same as x = x / 5
print(x)  # 2.0

# modulus
x = 10
x //= 5  # this is the same as x = x // 5
print(x)  # 2

# exponentiation
x = 10
x %= 5  # this is the same as x = x % 5
print(x)  # 0

# integer division
x = 10
x **= 5  # this is the same as x = x ** 5
print(x)  # 100000

# bitwise AND
x = 10
x &= 5  # this is the same as x = x & 5
print(x)  # 0

# bitwise OR
x = 10
x |= 5  # this is the same as x = x | 5
print(x)  # 15

# bitwise XOR
x = 10
x ^= 5  # this is the same as x = x ^ 5
print(x)  # 15

# bitwise NOT
x = 10
x >>= 5  # this is the same as x = x >> 5
print(x)  # 0

# bitwise left shift
x = 10
x <<= 5  # this is the same as x = x << 5
print(x)  # 320

# bitwise right shift
x = 10
x = ~x  # this is the same as x = x ~ 5
print(x)  # -11

