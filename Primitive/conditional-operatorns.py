# 🧑🏽‍🔧 Conditional Operators: basically if-else statements, loops and arithmetic operators.

# == > Equal to
# != > Not equal to
# > > Greater than
# < > Less than
# >= > Greater than or equal to
# <= > Less than or equal to
# is > Object identity

x = 'Hello'
y = 'World'

print(x == y)  # False

h = 10
u = 'World'

if (h == u):
    print('Hello')
else:
    print('Data type is not equal')


e = 100
q = 1000000

if (e >= q):
    print('Hello')
else:
    print('World')
