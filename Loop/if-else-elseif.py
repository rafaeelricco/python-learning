# ðŸ—½ If, Else and Elif Statements: the if-elif-else statement is used to conditionally execute a statement or a block of statements. Conditions can be true or false, execute one thing when the condition is true, something else when the condition is false.

x = 'John Doe'

if (x == 'John Doe'):
    print('it is John Doe')
elif (x != 'John Doe'):
    print('it is not John Doe')
else:
    print('any other name')
