# str
x = "Hello World"

# numeric types
x = 20  # int
x = 20.5  # float
x = 1j  # complex

# sequence types
x = ["apple", "banana", "cherry"]  # list
x = ("apple", "banana", "cherry")  # tuple
x = range(6)  # range

# mapping types
x = {"name": "John", "age": 36, "weight": 80.5}  # dict

# set types
x = {"apple", "banana", "cherry"}  # set
x = frozenset({"apple", "banana", "cherry"})  # frozenset

# boolean types
x = True  # bool

# binary types
x = b"Hello"  # bytes
x = bytearray(5)  # bytearray
x = memoryview(bytes(5))  # memoryview
x = None  # NoneType
